#include "libft.h"

intmax_t	ft_get_str_len(intmax_t n)
{
	intmax_t		i;
	intmax_t		k;

	k = n < 0 ? 1 : 0;
	i = 1;
	while (n /= 10)
		i++;
	return (i + k);
}
